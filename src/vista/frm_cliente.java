    //Activa la acción en el botón modificar
    private void btn_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_actualizarActionPerformed
        // TODO add your handling code here:
        String id_puesto= combo_puesto.getSelectedItem().toString();
        int puesto=id_puesto.indexOf(')');
           
        empleado = new empleados((Integer.valueOf(lbl_id.getText())),this.txt_codigo.getText(), Integer.parseInt(id_puesto.substring(0, puesto)), this.txt_nombres.getText(),  this.txt_apellidos.getText(),this.txt_direccion.getText(), this.txt_telefono.getText(), this.txt_fn.getText());
        empleado.actualizar();
        tbl_datos.setModel(empleado.leer());
        limpiar();
    }//GEN-LAST:event_btn_actualizarActionPerformed

     //Activa la acción en el botón agregar
    private void bt_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bt_agregarActionPerformed
        // TODO add your handling code here:
        //Crea un cliente nuevo con los campos de texto
        int puesto = combo_puesto.getSelectedIndex();
        empleado = new empleados(0,this.txt_codigo.getText(), puesto, this.txt_nombres.getText(),  this.txt_apellidos.getText(),this.txt_direccion.getText(), this.txt_telefono.getText(), this.txt_fn.getText());
        empleado.agregar();
        tbl_datos.setModel(empleado.leer());
        limpiar();
    }//GEN-LAST:event_bt_agregarActionPerformed

