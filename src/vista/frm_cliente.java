    private void btn_eliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_eliminarActionPerformed
        // TODO add your handling code here:
        empleado = new empleados();
        empleado.setId(Integer.valueOf(this.lbl_id.getText()));
        empleado.borrar();
        tbl_datos.setModel(empleado.leer());
        limpiar();
   
    }//GEN-LAST:event_btn_eliminarActionPerformed

    private void selectDatos(){
        int fila = this.tbl_datos.getSelectedRow();
        lbl_id.setText(tbl_datos.getValueAt(fila, 0).toString());
        this.txt_codigo.setText(tbl_datos.getValueAt(fila, 1).toString());
        this.txt_nombres.setText(tbl_datos.getValueAt(fila, 2).toString());
        this.txt_apellidos.setText(tbl_datos.getValueAt(fila, 3).toString());
        this.txt_direccion.setText(tbl_datos.getValueAt(fila, 4).toString());
        this.txt_telefono.setText(tbl_datos.getValueAt(fila, 5).toString());
        this.txt_fn.setText(tbl_datos.getValueAt(fila, 6).toString());
        String puestoSeleccionado = tbl_datos.getValueAt(fila, 7).toString(); // Suponiendo que el ID del puesto está en la columna 7

        // Establecer el puesto en el JComboBox
        combo_puesto.setSelectedItem(puestoSeleccionado);
    }
    
